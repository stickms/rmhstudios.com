import { createFileRoute } from '@tanstack/react-router';
import { auth } from '@/lib/auth';
import { rateLimit, getClientIp } from '@/lib/rate-limit';
import { prisma } from '@/lib/prisma.server';
import { putObject, deleteObject } from '@/lib/storage/s3.server';
import { validateImageBuffer } from '@/lib/slice-it/upload-validation';
import { processLibraryUpload, type UploadDeps } from '@/lib/library/upload';
import { uploadSlugExists } from '@/lib/library/library.server';
import { libraryPdfKey, libraryCoverKey, libraryPdfUrl } from '@/lib/library/keys';
import { compressPdfForStorage } from '@/lib/library/compress.server';

const COVER_MAX_BYTES = 2 * 1024 * 1024; // 2 MB

export const Route = createFileRoute('/api/library/upload')({
  server: {
    handlers: {
      POST: async ({ request }) => {
        try {
          const session = await auth.api.getSession({ headers: request.headers });
          if (!session) {
            return Response.json({ error: 'You must be signed in to upload.' }, { status: 401 });
          }
          const userId = session.user.id;
          const isAdmin = Boolean((session.user as { isAdmin?: boolean }).isAdmin);

          const ip = getClientIp(request);
          const { allowed, retryAfter } = rateLimit(ip, {
            limit: 5,
            windowMs: 10 * 60_000,
            prefix: 'library-upload',
          });
          if (!allowed) {
            return Response.json(
              { error: "You've hit the upload limit. Try again later." },
              { status: 429, headers: { 'Retry-After': String(retryAfter) } }
            );
          }

          const form = await request.formData();
          const pdfFile = form.get('pdf');
          if (!(pdfFile instanceof File) || pdfFile.size === 0) {
            return Response.json({ error: 'No PDF provided.' }, { status: 400 });
          }
          const pdf = Buffer.from(await pdfFile.arrayBuffer());

          // Cover is optional and self-generated by our client; validate leniently.
          let cover: Buffer | null = null;
          const coverFile = form.get('cover');
          if (coverFile instanceof File && coverFile.size > 0 && coverFile.size <= COVER_MAX_BYTES) {
            const buf = Buffer.from(await coverFile.arrayBuffer());
            if (validateImageBuffer(buf).ok) cover = buf;
          }

          const title = String(form.get('title') ?? '');
          const description = String(form.get('description') ?? '');
          const pages = Number(form.get('pages') ?? 0);

          // Pre-generate the id so we can clean up storage if persistence fails.
          const id = crypto.randomUUID();

          const deps: UploadDeps = {
            putObject,
            createDoc: async (data) => {
              await prisma.libraryDocument.create({
                data: {
                  id: data.id,
                  slug: data.slug,
                  title: data.title,
                  description: data.description,
                  pages: data.pages,
                  pdfKey: data.pdfKey,
                  coverKey: data.coverKey,
                  sizeBytes: data.sizeBytes,
                  uploadedByUserId: data.uploadedByUserId,
                  official: data.official,
                },
              });
              return { slug: data.slug };
            },
            countUserDocs: (uid) => prisma.libraryDocument.count({ where: { uploadedByUserId: uid } }),
            slugExists: uploadSlugExists,
            newId: () => id,
            compress: compressPdfForStorage,
          };

          let result;
          try {
            result = await processLibraryUpload(deps, {
              userId,
              pdf,
              cover,
              title,
              description,
              pages,
              isAdmin,
            });
          } catch (err) {
            // Persistence failed after objects may have been written — clean up.
            await deleteObject(libraryPdfKey(id)).catch(() => {});
            await deleteObject(libraryCoverKey(id)).catch(() => {});
            throw err;
          }

          if (!result.ok) {
            return Response.json({ error: result.error }, { status: result.status });
          }
          return Response.json({ slug: result.slug, url: libraryPdfUrl(id) }, { status: 201 });
        } catch (error) {
          console.error('Library upload error:', error);
          return Response.json({ error: 'Upload failed, nothing was saved.' }, { status: 500 });
        }
      },
    },
  },
});
