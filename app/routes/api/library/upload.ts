import { createFileRoute } from '@tanstack/react-router';
import { auth } from '@/lib/auth';
import { rateLimit, getClientIp } from '@/lib/rate-limit';
import { prisma } from '@/lib/prisma.server';
import { putObject, deleteObject, objectExists, s3Configured } from '@/lib/storage/s3.server';
import { validateImageBuffer } from '@/lib/slice-it/upload-validation';
import { processLibraryUpload, type UploadDeps } from '@/lib/library/upload';
import { libraryPdfMaxBytes } from '@/lib/library/upload-validation';
import { uploadSlugExists } from '@/lib/library/library.server';
import { effectiveQuota } from '@/lib/library/quota.server';
import { libraryFileKey, libraryCoverKey, libraryPdfUrl } from '@/lib/library/keys';
import { compressPdfForStorage } from '@/lib/library/compress.server';
import { logAdminAction } from '@/lib/admin-audit.server';

const COVER_MAX_BYTES = 2 * 1024 * 1024; // 2 MB

export const Route = createFileRoute('/api/library/upload')({
  server: {
    handlers: {
      POST: async ({ request }) => {
        try {
          const session = await auth.api.getSession({ headers: request.headers });
          if (!session) {
            return Response.json({ error: 'You must be signed in to upload.' }, { status: 401 });
          }
          const userId = session.user.id;
          const isAdmin = Boolean((session.user as { isAdmin?: boolean }).isAdmin);

          // Library files MUST go to durable, shared object storage. Without S3 the
          // storage layer silently falls back to the local filesystem — fine for a
          // single dev box, but in a multi-instance / ephemeral deploy the bytes land
          // on one container's disk and 404 once that container recycles or another
          // instance serves the read. Refuse rather than accept an upload we can't
          // reliably serve back. A dev can opt into local storage explicitly.
          if (!s3Configured() && process.env.LIBRARY_ALLOW_LOCAL_STORAGE !== 'true') {
            console.error('Library upload refused: object storage (S3_*) is not configured — uploads would not persist.');
            return Response.json(
              {
                error:
                  'Uploads are disabled: durable object storage is not configured on the server. Set the S3_* environment variables (Cloudflare R2).',
              },
              { status: 503 },
            );
          }

          // Admins are unlimited; regular users have a modest per-IP rate limit.
          if (!isAdmin) {
            const ip = getClientIp(request);
            const { allowed, retryAfter } = rateLimit(ip, {
              limit: 30,
              windowMs: 10 * 60_000,
              prefix: 'library-upload',
            });
            if (!allowed) {
              return Response.json(
                { error: "You've hit the upload limit. Try again later." },
                { status: 429, headers: { 'Retry-After': String(retryAfter) } }
              );
            }
          }

          // Reject oversized uploads by their declared size BEFORE buffering the
          // whole body into memory. The admin ceiling + cover + a little multipart
          // overhead is the hard cap; per-account limits are re-checked precisely
          // inside processLibraryUpload once the bytes are known.
          const declaredLength = Number(request.headers.get('content-length') ?? 0);
          const maxUploadBytes = libraryPdfMaxBytes(true) + COVER_MAX_BYTES + 2 * 1024 * 1024;
          if (Number.isFinite(declaredLength) && declaredLength > maxUploadBytes) {
            return Response.json({ error: 'File too large.' }, { status: 413 });
          }

          const form = await request.formData();
          // Accept either the new `file` field or the legacy `pdf` field.
          const bookFile = form.get('file') ?? form.get('pdf');
          if (!(bookFile instanceof File) || bookFile.size === 0) {
            return Response.json({ error: 'No file provided.' }, { status: 400 });
          }
          const file = Buffer.from(await bookFile.arrayBuffer());

          // Cover is optional and self-generated by our client; validate leniently.
          let cover: Buffer | null = null;
          const coverFile = form.get('cover');
          if (coverFile instanceof File && coverFile.size > 0 && coverFile.size <= COVER_MAX_BYTES) {
            const buf = Buffer.from(await coverFile.arrayBuffer());
            if (validateImageBuffer(buf).ok) cover = buf;
          }

          const title = String(form.get('title') ?? '');
          const description = String(form.get('description') ?? '');
          const pages = Number(form.get('pages') ?? 0);

          // Pre-generate the id so we can clean up storage if persistence fails.
          const id = crypto.randomUUID();

          const deps: UploadDeps = {
            putObject,
            createDoc: async (data) => {
              await prisma.libraryDocument.create({
                data: {
                  id: data.id,
                  slug: data.slug,
                  title: data.title,
                  description: data.description,
                  pages: data.pages,
                  format: data.format,
                  pdfKey: data.pdfKey,
                  coverKey: data.coverKey,
                  sizeBytes: data.sizeBytes,
                  uploadedByUserId: data.uploadedByUserId,
                  official: data.official,
                },
              });
              return { slug: data.slug };
            },
            countUserDocs: (uid) => prisma.libraryDocument.count({ where: { uploadedByUserId: uid } }),
            slugExists: uploadSlugExists,
            newId: () => id,
            compress: compressPdfForStorage,
          };

          // Effective per-account cap (admins are uncapped and skip this).
          const quota = isAdmin ? undefined : await effectiveQuota(userId);

          let result;
          try {
            result = await processLibraryUpload(deps, {
              userId,
              file,
              cover,
              title,
              description,
              pages,
              isAdmin,
              quota,
            });
          } catch (err) {
            // Persistence failed after objects may have been written — clean up both
            // possible keys (format is detected inside processLibraryUpload).
            await deleteObject(libraryFileKey(id, 'pdf')).catch(() => {});
            await deleteObject(libraryFileKey(id, 'epub')).catch(() => {});
            await deleteObject(libraryCoverKey(id)).catch(() => {});
            throw err;
          }

          if (!result.ok) {
            return Response.json({ error: result.error }, { status: result.status });
          }

          // Confirm the stored file is actually retrievable before telling the client
          // it worked. A silent storage miss here becomes an honest error now, instead
          // of a 404 the next time someone opens the book.
          const stored =
            (await objectExists(libraryFileKey(id, 'pdf')).catch(() => false)) ||
            (await objectExists(libraryFileKey(id, 'epub')).catch(() => false));
          if (!stored) {
            console.error(`Library upload ${id}: stored object not found after write — rolling back.`);
            await prisma.libraryDocument.delete({ where: { id } }).catch(() => {});
            await deleteObject(libraryCoverKey(id)).catch(() => {});
            return Response.json(
              { error: 'The file could not be stored. Please try again.' },
              { status: 502 },
            );
          }

          // Admin uploads land in the curated section — record them in the audit log.
          if (isAdmin) {
            await logAdminAction(userId, 'library.upload', {
              targetType: 'LibraryDocument',
              targetId: id,
              detail: title.trim().slice(0, 120),
            });
          }
          return Response.json({ slug: result.slug, url: libraryPdfUrl(id) }, { status: 201 });
        } catch (error) {
          console.error('Library upload error:', error);
          return Response.json({ error: 'Upload failed, nothing was saved.' }, { status: 500 });
        }
      },
    },
  },
});
